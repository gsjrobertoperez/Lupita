    public void validateInput() throws ValidationException {


        boolean isValidamount = InputValidator.validateText2(amountArtText);
        boolean isValidlprice = InputValidator.validateText2(priceText);

        if (isValidamount || isValidlprice) {

            throw new ValidationException(InputValidator.GENERAL_ERROR);
        }


    }

    private void addPurchaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addPurchaseActionPerformed
        try {
            validateInput();
        } catch (ValidationException ex) {
            
            JOptionPane.showMessageDialog(this.getParent(), ex.getMessage());
            amountArtText.setForeground(Color.black);
        }

        int amount = Integer.parseInt(amountArtText.getText());
        double price = Double.parseDouble(priceText.getText());
        Purchase purchase = new Purchase(model.getSelectedArticle().getDescripcion(), model.getSelectedArticle().getid_A(), amount, price, Constants.getCurrentDate());
        model.addPurchase(purchase);

        updateTableModel();


    }//GEN-LAST:event_addPurchaseActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {

            if (model.getPurchasesSize() == 0) {
                JOptionPane.showMessageDialog(this, "No tiene ninguna Compra registrada");
            } else {
                model.insertPuchases();
                JOptionPane.showMessageDialog(this, "La compra ha sido registrada exitosamente");
            }//else
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void deletePurchaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletePurchaseActionPerformed

        if (model.getSelectedIndex() == null) {
            JOptionPane.showMessageDialog(this, "No tiene compra seleccionada");
        } else if (Constants.confirmDialog(this, "Está seguro de que desea eliminar la compra seleccionada? ")) {


            model.removePurchase(); 
            updateTableModel();
            model.setSelectedIndex(null);
        }


    }//GEN-LAST:event_deletePurchaseActionPerformed

