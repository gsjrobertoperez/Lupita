    private void updateTableModel() {

        DefaultTableModel tableModel = new DefaultTableModel();
        int total = Integer.parseInt(amountArtText.getText());
        tableModel.addColumn("Cantidad");
        tableModel.addColumn("Descripción");
        tableModel.addColumn("Total de Compra");


        for (int i = 0; i < model.getPurchases().size(); i++) {
            Purchase tmp = model.getPurchases().get(i);

            tableModel.addRow(new String[]{Integer.toString(tmp.getcantidad_Art()), tmp.getDesc(), Double.toString(tmp.getprecio_C() * total)});
        }

        this.purchasesPanel.setModel(tableModel);


    }
